/* Current language — persisted in localStorage */
function getCurrentLang() {
  const saved = (typeof localStorage !== 'undefined') ? localStorage.getItem('knkLang') : null;
  if (saved && T[saved]) return saved;
  const nav = (typeof navigator !== 'undefined' ? navigator.language : 'en').split('-')[0];
  if (T[nav]) return nav;
  return 'en';
}

function setLang(code) {
  if (!T[code]) return;
  try { localStorage.setItem('knkLang', code); } catch(e) {}
  applyLang(code);
}

function applyLang(code) {
  const dict = T[code] || T.en;
  document.documentElement.setAttribute('lang', code);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const spec = el.getAttribute('data-i18n-attr'); // "attr:key,attr:key"
    spec.split(',').forEach(pair => {
      const [attr, key] = pair.split(':').map(s => s.trim());
      if (dict[key] !== undefined) el.setAttribute(attr, dict[key].replace(/<[^>]+>/g, ''));
    });
  });
  // update the active option in the language menu
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === code);
  });
  const btnText = document.querySelector('.lang-btn-code');
  if (btnText) btnText.textContent = code.toUpperCase();
  // notify listeners
  if (window.dispatchEvent) window.dispatchEvent(new CustomEvent('langchange', { detail: { code } }));
}

function buildLangSwitcher(container) {
  if (!container) return;
  container.classList.add('lang-switch');
  container.innerHTML = `
    <button class="lang-btn" type="button" aria-label="Language" onclick="toggleLangMenu(event)">
      <svg viewBox="0 0 24 24"><path d="M12 2a10 10 0 1 0 10 10A10 10 0 0 0 12 2Zm7.93 9h-3.1a15.65 15.65 0 0 0-1.23-5.36A8 8 0 0 1 19.93 11ZM12 4a13.7 13.7 0 0 1 1.66 7H10.34A13.7 13.7 0 0 1 12 4ZM4.26 14a8 8 0 0 1 0-4h3.38a17 17 0 0 0 0 4ZM5.08 16h2.95a15.65 15.65 0 0 0 1.23 5.36A8 8 0 0 1 5.08 16ZM8.17 11H5.08a8 8 0 0 1 4.18-5.36A15.65 15.65 0 0 0 8.03 11ZM12 20a13.7 13.7 0 0 1-1.66-7h3.32A13.7 13.7 0 0 1 12 20Zm2.74-.64A15.65 15.65 0 0 0 15.97 14h2.95a8 8 0 0 1-4.18 5.36ZM16.36 12a17 17 0 0 0 0-4h3.38a8 8 0 0 1 0 4Z"/></svg>
      <span class="lang-btn-code">EN</span>
    </button>
    <div class="lang-menu">
      ${Object.values(LANGS).map(l => `
        <button class="lang-option" type="button" data-lang="${l.code}" onclick="setLang('${l.code}'); closeLangMenu()">
          ${l.name}<span class="lang-native">· ${l.native}</span>
        </button>
      `).join('')}
    </div>
  `;
}

function toggleLangMenu(e) {
  if (e && e.stopPropagation) e.stopPropagation();
  const el = document.querySelector('.lang-switch');
  if (el) el.classList.toggle('open');
}
function closeLangMenu() {
  const el = document.querySelector('.lang-switch');
  if (el) el.classList.remove('open');
}

// Close the language menu when clicking outside
if (typeof document !== 'undefined') {
  document.addEventListener('click', (e) => {
    const sw = document.querySelector('.lang-switch');
    if (sw && sw.classList.contains('open') && !sw.contains(e.target)) {
      sw.classList.remove('open');
    }
  });
}

